#!/usr/bin/env node
/**
 * Sync the monorepo CHANGELOG.md into a TypeScript module so the
 * frontend can `import` it without runtime fetch / webpack raw-loader
 * configuration. Run automatically via `predev` and `prebuild`.
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const CHANGELOG_PATH = resolve(__dirname, '..', '..', '..', 'CHANGELOG.md');
const OUTPUT_PATH = resolve(__dirname, '..', 'src', 'data', 'changelog.generated.ts');

async function main() {
  let raw;
  try {
    raw = await readFile(CHANGELOG_PATH, 'utf8');
  } catch (err) {
    console.error(`[generate-changelog] Could not read ${CHANGELOG_PATH}:`, err.message);
    process.exit(1);
  }

  const escaped = raw.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
  const banner = '// AUTO-GENERATED by scripts/generate-changelog.mjs from CHANGELOG.md.\n// Do not edit by hand. Run `pnpm --filter frontend run sync:changelog` to refresh.\n';
  const body = `${banner}\nconst CHANGELOG_MARKDOWN = \`${escaped}\`;\n\nexport default CHANGELOG_MARKDOWN;\n`;

  await mkdir(dirname(OUTPUT_PATH), { recursive: true });
  await writeFile(OUTPUT_PATH, body, 'utf8');
  console.log(`[generate-changelog] Wrote ${OUTPUT_PATH} (${raw.length} chars).`);
}

main().catch((err) => {
  console.error('[generate-changelog] Failed:', err);
  process.exit(1);
});
